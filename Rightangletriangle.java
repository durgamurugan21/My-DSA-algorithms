class Rightangletriangle
{
    public static void main (String args[])
    {
        int row=10,number;
            for(int i=0;i<row;i++)
            {
               number=1;
                
                for(int j=0;j<=i;j++)
                {
					System.out.print( number+ "");
					number++;
                }
                 System.out.println();
            }
          
    }
}
